const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

function decode(expr) {
  const arrLetterCode = expr.match(/.{1,10}/g);

  let arrMorseSymbol = [];
  arrLetterCode.map((el) => {
    el = el.replace(/10/g, ".").replace(/11/g, "-").replace(/0/g, "");
    arrMorseSymbol.push(MORSE_TABLE[el] || " ");
  });

  return arrMorseSymbol.join("");
}

module.exports = {
  decode,
};
